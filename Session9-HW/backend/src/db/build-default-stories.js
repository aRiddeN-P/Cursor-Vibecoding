function buildDefaultStories() {
  const db = createDatabase();
  const existing = db
    .prepare(
      `SELECT title, content, age_group, theme, emoji
       FROM stories WHERE is_custom = 0 ORDER BY id`
    )
    .all();
  db.close();

  const existingTitles = new Set(existing.map((s) => s.title));
  const merged = [...existing];

  for (const story of MISSING_STORIES) {
    if (!existingTitles.has(story.title)) {
      merged.push(story);
    }
  }

  return merged;
}

if (require.main === module) {
  const outPath = path.join(__dirname, 'default-stories.json');
  const stories = buildDefaultStories();
  fs.writeFileSync(outPath, JSON.stringify(stories, null, 2), 'utf8');
  console.log(`Wrote ${stories.length} stories to ${outPath}`);
}

module.exports = { buildDefaultStories, MISSING_STORIES };
